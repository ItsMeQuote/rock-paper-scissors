import random

варианты = ["камень", "ножницы", "бумага"]

# На старте просто спрашиваем первый ход
выбор_игрока = input("У вас есть варианты: камень, ножницы, бумага или 'выход'. Выберите один: ").lower()

while выбор_игрока != "выход":
    # 1. КОМПЬЮТЕР ДЕЛАЕТ НОВЫЙ ХОД ТУТ (В НАЧАЛЕ РАУНДА)
    выбор_компьютера = random.choice(варианты)
    print(f"Компьютер выбрал: {выбор_компьютера}")
    
    # 2. ПРОВЕРКА КТО КОГО
    if выбор_игрока not in варианты:
        print("⚠️ Ошибка: такого варианта нет. Попробуй: камень, ножницы или бумага.")

    elif выбор_игрока == выбор_компьютера:
        print("🤝 Ничья!")
        
    elif выбор_игрока == "камень" and выбор_компьютера == "ножницы":
        print("🎉 Ты выиграл раунд!")

    elif выбор_игрока == "ножницы" and выбор_компьютера == "бумага":
        print("🎉 Ты выиграл раунд!")
        
    elif выбор_игрока == "бумага" and выбор_компьютера == "камень":
        print("🎉 Ты выиграл раунд!")
    
    else:
        print("❌ Ты проиграл раунд!")
    
    # 3. КОНЕЦ РАУНДА (СПРАШИВАЕМ НОВЫЙ ХОД И ВСЁ)
    print("------------------")
    print("Изучай Telegram здесь: https://telegramm.fandom.com/ru/wiki/WikiGram")
    print("------------------")
    
    print("Новый раунд!")
    выбор_игрока = input("Ваш выбор: ").lower()

print("Игра завершена!")